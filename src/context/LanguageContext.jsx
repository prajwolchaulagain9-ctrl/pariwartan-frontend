export const useLanguage = () => {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error('useLanguage must be used within LanguageProvider');
  return ctx;
};

export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState(() => localStorage.getItem('uiLanguage') || 'en');

  const changeLanguage = (next) => {
    const safe = next === 'ne' ? 'ne' : 'en';
    setLanguage(safe);
    localStorage.setItem('uiLanguage', safe);
    document.documentElement.setAttribute('lang', safe === 'ne' ? 'ne' : 'en');
  };

  useEffect(() => {
    document.documentElement.setAttribute('lang', language === 'ne' ? 'ne' : 'en');
  }, [language]);

  const t = (text) => {
    if (language !== 'ne') return text;
    return DICTIONARY[text] || text;
  };

  const value = useMemo(() => ({ language, changeLanguage, t }), [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
};
